from fpdf import FPDF

FONT = "C:/Windows/Fonts/simhei.ttf"

class PDF(FPDF):
    def __init__(self):
        super().__init__()
        self.add_font("CJK", "", FONT)
        self.add_font("CJK", "B", FONT)

    def footer(self):
        self.set_y(-15)
        self.set_font("CJK", "", 7)
        self.set_text_color(128, 128, 128)
        self.cell(0, 10, f"- {self.page_no()} -", align="C")

    def title_block(self, text):
        """深色标题块"""
        self.ln(2)
        self.set_fill_color(15, 23, 42)
        self.set_text_color(255, 255, 255)
        self.set_font("CJK", "B", 12)
        self.multi_cell(self.w - self.l_margin - self.r_margin, 8, text, fill=True)
        self.ln(3)

    def h2(self, text):
        """二级标题"""
        self.ln(2)
        self.set_text_color(30, 64, 175)
        self.set_font("CJK", "B", 11)
        self.cell(0, 7, text)
        self.ln(8)

    def h3(self, text):
        """三级标题"""
        self.ln(1)
        self.set_text_color(50, 50, 50)
        self.set_font("CJK", "B", 10)
        self.cell(0, 6.5, text)
        self.ln(7.5)

    def p(self, text):
        """正文段落"""
        self.set_text_color(40, 40, 40)
        self.set_font("CJK", "", 9)
        self.multi_cell(self.w - self.l_margin - self.r_margin, 5.5, text)
        self.ln(1)

    def bullet(self, text):
        """项目符号"""
        self.set_text_color(40, 40, 40)
        self.set_font("CJK", "", 9)
        self.cell(6, 5.5, "")
        self.set_font("CJK", "", 9)
        self.cell(4, 5.5, ">")
        self.multi_cell(self.w - self.l_margin - self.r_margin - 10, 5.5, text)

    def bold_line(self, text):
        """加粗行"""
        self.set_text_color(40, 40, 40)
        self.set_font("CJK", "B", 9)
        self.multi_cell(self.w - self.l_margin - self.r_margin, 5.5, text)
        self.ln(1)

    def highlight_box(self, text, r=255, g=245, b=230):
        """高亮背景框"""
        self.set_fill_color(r, g, b)
        self.set_text_color(60, 40, 0)
        self.set_font("CJK", "B", 9)
        self.multi_cell(self.w - self.l_margin - self.r_margin, 6, text, fill=True)
        self.ln(2)

    def table(self, headers, rows, widths):
        """渲染表格"""
        # header
        self.set_fill_color(30, 64, 175)
        self.set_text_color(255, 255, 255)
        self.set_font("CJK", "B", 7.5)
        for h, w in zip(headers, widths):
            self.cell(w, 7, " " + h, border=1, fill=True)
        self.ln(8)
        # rows
        for i, row in enumerate(rows):
            if i % 2 == 0:
                self.set_fill_color(245, 248, 255)
            else:
                self.set_fill_color(255, 255, 255)
            self.set_text_color(40, 40, 40)
            self.set_font("CJK", "", 7.5)
            for cell, w in zip(row, widths):
                self.cell(w, 6, " " + str(cell), border=1, fill=True)
            self.ln(7)


